#include "libft.h"

char	*ft_isintab(const char *s, const char *c)
{
	int	i;
	int i2;

	i = 0;
	while (s[i])
	{
		i2 = 0;
		while (c[i2])
		{
			if (c[i2] == s[i])
				return ((char *)s + i);
			i2++;
		}
		i++;
	}
	return (0);
}
